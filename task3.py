# TrendPulse - Task 3
# Goal: Analyse the cleaned CSV using NumPy and Pandas

import pandas as pd
import numpy as np

# --- Step 1: Load the cleaned CSV ---

df = pd.read_csv("hackernews_clean.csv")

print("=== Data Loaded ===")
print(df.head())           # first 5 rows
print("\nShape:", df.shape)  # rows and columns

# --- Step 2: Basic statistics using NumPy ---

scores = df['score'].values        # convert column to NumPy array
comments = df['num_comments'].values

print("\n=== Score Statistics ===")
print("Mean score:  ", round(np.mean(scores), 2))      # average
print("Median score:", round(np.median(scores), 2))    # middle value
print("Max score:   ", np.max(scores))                 # highest
print("Min score:   ", np.min(scores))                 # lowest
print("Std Dev:     ", round(np.std(scores), 2))       # how spread out scores are

print("\n=== Comment Statistics ===")
print("Mean comments:  ", round(np.mean(comments), 2))
print("Median comments:", round(np.median(comments), 2))
print("Max comments:   ", np.max(comments))
print("Min comments:   ", np.min(comments))

# --- Step 3: Highest scored post ---

top_post = df[df['score'] == df['score'].max()]
print("\n=== Highest Scored Post ===")
print("Title:", top_post['title'].values[0])
print("Score:", top_post['score'].values[0])
print("Author:", top_post['author'].values[0])

# --- Step 4: Most active author ---

top_author = df['author'].value_counts().idxmax()  # author with most posts
print("\n=== Most Active Author ===")
print("Author:", top_author)
print("Posts:", df['author'].value_counts().max())

# --- Step 5: Most commented post ---

top_comment = df[df['num_comments'] == df['num_comments'].max()]
print("\n=== Most Commented Post ===")
print("Title:", top_comment['title'].values[0])
print("Comments:", top_comment['num_comments'].values[0])

# --- Step 6: Score distribution ---

# Split scores into ranges: 0-100, 100-300, 300-600, 600+
bins = [0, 100, 300, 600, float('inf')]
labels = ['Low (0-100)', 'Medium (100-300)', 'High (300-600)', 'Viral (600+)']

df['score_category'] = pd.cut(df['score'], bins=bins, labels=labels)

print("\n=== Score Distribution ===")
print(df['score_category'].value_counts())

# --- Step 7: Correlation between score and comments ---

correlation = np.corrcoef(scores, comments)[0][1]
print("\n=== Correlation: Score vs Comments ===")
print(f"Correlation value: {round(correlation, 2)}")
print("(1.0 = perfect match, 0 = no relation, -1 = opposite)")

# --- Step 8: Save analysis summary as CSV ---

summary = pd.DataFrame({
    'metric': ['mean_score', 'median_score', 'max_score', 'min_score',
               'mean_comments', 'max_comments', 'score_comment_correlation'],
    'value': [round(np.mean(scores), 2), round(np.median(scores), 2),
              np.max(scores), np.min(scores),
              round(np.mean(comments), 2), np.max(comments),
              round(correlation, 2)]
})

summary.to_csv("hackernews_analysis.csv", index=False)
print("\n✅ Saved analysis to hackernews_analysis.csv")

